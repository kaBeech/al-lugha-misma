CREATE TABLE IF NOT EXISTS languages_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    language TEXT NOT NULL,
    language_http_friendly TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS word_lists_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    word_list TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS word_lists_custom_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    word_list_custom TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS reference_words_english_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    word_list int NOT NULL REFERENCES word_lists_sql_test (id),
    reference_word_english text NOT NULL
);
CREATE TABLE IF NOT EXISTS word_lists_custom_associations_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    word_list_custom int NOT NULL REFERENCES word_lists_custom_sql_test (id),
    reference_word_english int NOT NULL REFERENCES reference_words_english_sql_test (id)
);
CREATE TABLE IF NOT EXISTS challenge_cards_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    word_list int NOT NULL REFERENCES word_lists_sql_test (id),
    language int NOT NULL REFERENCES languages_sql_test (id)
);
CREATE TABLE IF NOT EXISTS challenge_cards_custom_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    word_list_custom int NOT NULL REFERENCES word_lists_custom_sql_test (id),
    language int NOT NULL REFERENCES languages_sql_test (id)
);
CREATE TABLE IF NOT EXISTS challenge_decks_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    challenge_deck text NOT NULL
);
CREATE TABLE IF NOT EXISTS challenge_decks_custom_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    challenge_deck_custom text NOT NULL
);
CREATE TABLE IF NOT EXISTS challenge_deck_associations_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    challenge_card int NOT NULL REFERENCES challenge_cards_sql_test (id),
    challenge_deck int NOT NULL REFERENCES challenge_decks_sql_test (id)
);
CREATE TABLE IF NOT EXISTS challenge_deck_custom_associations_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    challenge_card_custom int NOT NULL REFERENCES challenge_cards_custom_sql_test (id),
    challenge_deck_custom int NOT NULL REFERENCES challenge_decks_custom_sql_test (id)
);
CREATE TABLE IF NOT EXISTS challenge_deck_record_times_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    challenge_deck int NOT NULL REFERENCES challenge_decks_sql_test (id),
    record_time_in_ms int NOT NULL 
);
CREATE TABLE IF NOT EXISTS challenge_deck_custom_record_times_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    challenge_deck_custom int NOT NULL REFERENCES challenge_decks_custom_sql_test (id),
    record_time_in_ms int NOT NULL 
);
CREATE TABLE IF NOT EXISTS transliterated_words_sql_test (
    id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    language int NOT NULL REFERENCES languages (id),
    reference_word_english int NOT NULL REFERENCES reference_words_english_sql_test (id),
    transliterated_word text NOT NULL
);

